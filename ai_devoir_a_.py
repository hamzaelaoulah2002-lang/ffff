class Taquin:
    def __init__(self, size = 3, initial = None):
        """
        Initialize a new Taquin game board.
        Examples
        --------
        # >>> t = Taquin()
        # >>> print(t)
        ...
        """
        # set the board size
        self.size = size
        self.final = np.arange(1, self.size**2+1)
        self.final[-1] = 0

        self.final = self.final.reshape(self.size, self.size)
        self.final = self.tostring(self.final)

        self.alt_initial = self.final#.copy()

        if initial is None:
            # generate a random board if no initial state is specified
            self.board = self.alt_initial
            self.board = self.reverse()
        # use the specified initial state
        else:
            self.board = initial


    def asgrid(self, board):
      # board = board.copy()
      if isinstance(board, str):
        board = board.split('.')
        grid = [int(c) for c in board]
        grid = np.array(grid)
      elif isinstance(board,np.ndarray ):
        grid = np.array(board)
      grid = grid.reshape((self.size, self.size))
      return grid

    def tostring(self, grid):
        grid = grid.flatten()
        string = [str(d) for d in grid]
        return '.'.join(string)

    def __str__(self):
        # return str(self.asgrid(self.board))
        # return self.board
        grid = self.asgrid(self.board)
        s=''
        cell_width = int(np.ceil(np.log10(self.size**2)))
        for i in range(self.size):
            p = '|'+f'{str(" | ")}'.join([f'{str(grid[i,j]):0>{cell_width}}' for j in range(self.size)])+'|'
            s = f'\n{(self.size*(cell_width+3)-1)*"-"}\n'.join([s, p])
        return f'\n{(self.size*(cell_width+3)-1)*"-"}\n'.join([s, ''])
    def wheretomove(self):
        grid = self.asgrid(self.board)
        empty_pos = np.argwhere(grid == 0)
        y,x = empty_pos[0, 0],empty_pos[0,1]

        children = []
        if y-1 in range(self.size):
            # move up U
            copy = grid.copy()
            copy[y,x] = copy[y-1,x]
            copy[y-1,x] = 0
            children.append(self.tostring(copy))
        if x-1 in range(self.size):
            # move left L
            copy = grid.copy()
            copy[y,x] = copy[y,x-1]
            copy[y,x-1] = 0
            children.append(self.tostring(copy))
        if y+1 in range(self.size):
            # move down D
            copy = grid.copy()
            copy[y,x] = copy[y+1,x]
            copy[y+1,x] = 0
            children.append(self.tostring(copy))
        if x+1 in range(self.size):
            # move right R
            copy = grid.copy()
            copy[y,x] = copy[y,x+1]
            copy[y,x+1] = 0
            children.append(self.tostring(copy))
        return   children
    def reverse(self):
      copy = self.asgrid(self.board)
      for _ in range(100): # Increased for better shuffle
          empty_pos = np.argwhere(copy == 0)[0]
          y, x = empty_pos

          moves = []
          if y > 0: moves.append((-1, 0)) # Up
          if y < self.size - 1: moves.append((1, 0)) # Down
          if x > 0: moves.append((0, -1)) # Left
          if x < self.size - 1: moves.append((0, 1)) # Right

          dy, dx = moves[np.random.randint(len(moves))]
          copy[y, x], copy[y+dy, x+dx] = copy[y+dy, x+dx], copy[y, x]

      return self.tostring(copy)


    def isfinal(self):
        return self.board == self.final

