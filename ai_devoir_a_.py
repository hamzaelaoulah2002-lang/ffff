import heapq

class algoAstar:
    def __init__(self, size=3):
        self.size = size

    def heuristic(self, state):
        return 0

    def get_path(self, sol):
        if sol is None:
            return ''
        else:
            taquin = Taquin(self.size, sol)
            return self.get_path(self.parents[sol]["parent"]) + '\n' + str(taquin)

    def get_depth(self, sol):
        if sol is None:
            return 0
        else:
            return 1 + self.get_depth(self.parents[sol]["parent"])

    def search(self):
        self.open_heap = []
        self.closed_nodes = set()
        self.parents = {}

        taquin = Taquin(self.size)
        start = taquin.board

        self.parents[start] = {"parent": None, "g": 0}

        f_start = self.parents[start]["g"] + self.heuristic(start)
        counter = 0
        heapq.heappush(self.open_heap, (f_start, counter, start))

        success = False
        i = 0
        solution = None

        while self.open_heap and not success:
            current_f, _, current = heapq.heappop(self.open_heap)

            if current in self.closed_nodes:
                continue

            i += 1
            self.closed_nodes.add(current)

            taquin = Taquin(self.size, current)

            if taquin.isfinal():
                success = True
                solution = current
            else:
                children = taquin.wheretomove()

                for child in children:
                    if child in self.closed_nodes:
                        continue

                    tentative_g = self.parents[current]["g"] + 1

                    if (child not in self.parents) or (tentative_g < self.parents[child]["g"]):
                        self.parents[child] = {"parent": current, "g": tentative_g}

                        f_child = tentative_g + self.heuristic(child)
                        counter += 1
                        heapq.heappush(self.open_heap, (f_child, counter, child))

        if success:
            print("success")
            print("Nombre de noeuds mémorisés :", len(self.parents))
            print(self.get_path(solution))
            print("Profondeur :", self.get_depth(solution) - 1)

        print("Noeuds explorés i =", i)

